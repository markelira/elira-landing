const admin = require('firebase-admin');

process.env.FIRESTORE_EMULATOR_HOST = '127.0.0.1:8080';

if (!admin.apps.length) {
  admin.initializeApp({ projectId: 'elira-landing-ce927' });
}

const db = admin.firestore();

(async () => {
  try {
    console.log('üîß Fixing enrollment document IDs...\n');

    // Get all enrollments
    const enrollmentsSnapshot = await db.collection('enrollments').get();

    if (enrollmentsSnapshot.empty) {
      console.log('‚ö†Ô∏è  No enrollments found!');
      process.exit(0);
    }

    console.log(`üìã Found ${enrollmentsSnapshot.size} enrollment documents\n`);

    let fixed = 0;
    let skipped = 0;

    for (const doc of enrollmentsSnapshot.docs) {
      const data = doc.data();
      const { userId, courseId } = data;

      if (!userId || !courseId) {
        console.log(`‚ö†Ô∏è  Skipping ${doc.id} - missing userId or courseId`);
        skipped++;
        continue;
      }

      const expectedId = `${userId}_${courseId}`;

      if (doc.id === expectedId) {
        console.log(`‚úÖ ${doc.id} - Already has correct ID format`);
        skipped++;
        continue;
      }

      console.log(`üîÑ Migrating: ${doc.id} ‚Üí ${expectedId}`);

      // Check if correct ID document already exists
      const correctDoc = await db.collection('enrollments').doc(expectedId).get();

      if (correctDoc.exists) {
        console.log(`   ‚ö†Ô∏è  Document ${expectedId} already exists, deleting duplicate ${doc.id}`);
        await doc.ref.delete();
        fixed++;
        continue;
      }

      // Create new document with correct ID
      await db.collection('enrollments').doc(expectedId).set({
        ...data,
        migratedAt: admin.firestore.FieldValue.serverTimestamp(),
        originalDocId: doc.id
      });

      // Delete old document
      await doc.ref.delete();

      console.log(`   ‚úÖ Successfully migrated to ${expectedId}`);
      fixed++;
    }

    console.log(`\n‚úÖ Migration complete!`);
    console.log(`   Fixed: ${fixed}`);
    console.log(`   Skipped: ${skipped}`);
    console.log(`   Total: ${enrollmentsSnapshot.size}\n`);

    process.exit(0);
  } catch (error) {
    console.error('‚ùå Error:', error);
    process.exit(1);
  }
})();
